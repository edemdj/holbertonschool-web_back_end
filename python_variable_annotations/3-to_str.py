#!/usr/bin/env python3
def to_str(n: float) -> str:
    """
    Prend un argument de type float et renvoie
    sa représentation sous forme de chaîne de caractères.

    Args:
        n (float): Le nombre float.

    Returns:
        str: La représentation sous forme de chaîne de caractères de n.
    """
    return str(n)
