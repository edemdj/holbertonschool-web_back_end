#!/usr/bin/env python3
def concat(str1: str, str2: str) -> str:
    """
    Prend deux arguments de type chaîne de caractères et renvoie leur concaténation en tant que chaîne de caractères.
    
    Args:
        str1 (str): La première chaîne de caractères.
        str2 (str): La deuxième chaîne de caractères.
        
    Returns:
        str: La concaténation de str1 et str2.
    """
    return str1 + str2
